int vc_hsv_segmentation(IVC* src, IVC* dst, int hmin, int hmax, int smin,	int smax, int vmin, int vmax)
{
	unsigned char* data = (unsigned char*)src->data;
	unsigned char* datadst = (unsigned char*)dst->data;
	int width = src->width;
	int height = src->height;
	int channels = src->channels;
	int hue, saturation, value;
	long int pos_src, pos_dst;
	int x, y;
	int bytesperline_src = src->width * src->channels;
	int bytesperline_dst = dst->width * dst->channels;

	// Verifica��o de erros
	if ((width <= 0) || (height <= 0) || (data == NULL) || datadst == NULL) return 0;
	if (width != dst->width || height != dst->height) return 0;
	if (channels != 3 || dst->channels != 1) return 0;

	for (y = 0; y < height; y++)
	{
		for (x = 0; x < width; x++)
		{
			pos_src = y * bytesperline_src + x * channels;
			pos_dst = y * bytesperline_dst + x;

			hue = (int)((float)data[pos_src] / 255.0f * 360.0f);
			saturation = (int)((float)data[pos_src + 1] / 255.0f * 100.0f);
			value = (int)((float)data[pos_src + 2] / 255.0f * 100.0f);

			if (hue >= hmin && hue <= hmax &&
				saturation >= smin && saturation <= smax &&
				value >= vmin && value <= vmax)
				datadst[pos_dst] = (unsigned char)255;
			else datadst[pos_dst] = (unsigned char)0;
		}
	}

	return 1;
}


OVC* vc_binary_blob_labelling(IVC* src, IVC* dst, int* nlabels)
{
	unsigned char* datasrc = (unsigned char*)src->data;
	unsigned char* datadst = (unsigned char*)dst->data;
	int width = src->width;
	int height = src->height;
	int bytesperline = src->bytesperline;
	int channels = src->channels;
	int x, y, a, b;
	long int i, size;
	long int posX, posA, posB, posC, posD;
	int labeltable[256] = { 0 };
	int labelarea[256] = { 0 };
	int label = 1; 
	int num, tmplabel;
	OVC* blobs; 


	if ((src->width <= 0) || (src->height <= 0) || (src->data == NULL)) return 0;
	if ((src->width != dst->width) || (src->height != dst->height) || (src->channels != dst->channels)) return NULL;
	if (channels != 1) return NULL;

	memcpy(datadst, datasrc, bytesperline * height);

	for (i = 0, size = bytesperline * height; i < size; i++)
	{
		if (datadst[i] != 0) datadst[i] = 255;
	}

	for (y = 0; y < height; y++)
	{
		datadst[y * bytesperline + 0 * channels] = 0;
		datadst[y * bytesperline + (width - 1) * channels] = 0;
	}
	for (x = 0; x < width; x++)
	{
		datadst[0 * bytesperline + x * channels] = 0;
		datadst[(height - 1) * bytesperline + x * channels] = 0;
	}


	for (y = 1; y < height - 1; y++)
	{
		for (x = 1; x < width - 1; x++)
		{

			posA = (y - 1) * bytesperline + (x - 1) * channels; 
			posB = (y - 1) * bytesperline + x * channels; 
			posC = (y - 1) * bytesperline + (x + 1) * channels; 
			posD = y * bytesperline + (x - 1) * channels; 
			posX = y * bytesperline + x * channels; 


			if (datadst[posX] != 0)
			{
				if ((datadst[posA] == 0) && (datadst[posB] == 0) && (datadst[posC] == 0) && (datadst[posD] == 0))
				{
					datadst[posX] = label;
					labeltable[label] = label;
					label++;
				}
				else
				{
					num = 255;


					if (datadst[posA] != 0) num = labeltable[datadst[posA]];

					if ((datadst[posB] != 0) && (labeltable[datadst[posB]] < num)) num = labeltable[datadst[posB]];

					if ((datadst[posC] != 0) && (labeltable[datadst[posC]] < num)) num = labeltable[datadst[posC]];
					if ((datadst[posD] != 0) && (labeltable[datadst[posD]] < num)) num = labeltable[datadst[posD]];


					datadst[posX] = num;
					labeltable[num] = num;
					if (datadst[posA] != 0)
					{
						if (labeltable[datadst[posA]] != num)
						{
							for (tmplabel = labeltable[datadst[posA]], a = 1; a < label; a++)
							{
								if (labeltable[a] == tmplabel)
								{
									labeltable[a] = num;
								}
							}
						}
					}
					if (datadst[posB] != 0)
					{
						if (labeltable[datadst[posB]] != num)
						{
							for (tmplabel = labeltable[datadst[posB]], a = 1; a < label; a++)
							{
								if (labeltable[a] == tmplabel)
								{
									labeltable[a] = num;
								}
							}
						}
					}
					if (datadst[posC] != 0)
					{
						if (labeltable[datadst[posC]] != num)
						{
							for (tmplabel = labeltable[datadst[posC]], a = 1; a < label; a++)
							{
								if (labeltable[a] == tmplabel)
								{
									labeltable[a] = num;
								}
							}
						}
					}
					if (datadst[posD] != 0)
					{
						if (labeltable[datadst[posD]] != num)
						{
							for (tmplabel = labeltable[datadst[posC]], a = 1; a < label; a++)
							{
								if (labeltable[a] == tmplabel)
								{
									labeltable[a] = num;
								}
							}
						}
					}
				}
			}
		}
	}

	for (y = 1; y < height - 1; y++)
	{
		for (x = 1; x < width - 1; x++)
		{
			posX = y * bytesperline + x * channels; 

			if (datadst[posX] != 0)
			{
				datadst[posX] = labeltable[datadst[posX]];
			}
		}
	}
	for (a = 1; a < label - 1; a++)
	{
		for (b = a + 1; b < label; b++)
		{
			if (labeltable[a] == labeltable[b]) labeltable[b] = 0;
		}
	}
	*nlabels = 0;
	for (a = 1; a < label; a++)
	{
		if (labeltable[a] != 0)
		{
			labeltable[*nlabels] = labeltable[a]; 
			(*nlabels)++; 
		}
	}

	if (*nlabels == 0) return NULL;

	blobs = (OVC*)calloc((*nlabels), sizeof(OVC));
	if (blobs != NULL)
	{
		for (a = 0; a < (*nlabels); a++) blobs[a].label = labeltable[a];
	}
	else return NULL;

	return blobs;
}





int vc_binary_blob_info(IVC* src, OVC* blobs, int nblobs)
{
	unsigned char* data = (unsigned char*)src->data;
	int width = src->width;
	int height = src->height;
	int bytesperline = src->bytesperline;
	int channels = src->channels;
	int x, y, i;
	long int pos;
	int xmin, ymin, xmax, ymax;
	long int sumx, sumy;

	if ((src->width <= 0) || (src->height <= 0) || (src->data == NULL)) return 0;
	if (channels != 1) return 0;

	for (i = 0; i < nblobs; i++)
	{
		xmin = width - 1;
		ymin = height - 1;
		xmax = 0;
		ymax = 0;

		sumx = 0;
		sumy = 0;

		blobs[i].area = 0;

		for (y = 1; y < height - 1; y++)
		{
			for (x = 1; x < width - 1; x++)
			{
				pos = y * bytesperline + x * channels;

				if (data[pos] == blobs[i].label)
				{
					blobs[i].area++;
					sumx += x;
					sumy += y;
					if (xmin > x) xmin = x;
					if (ymin > y) ymin = y;
					if (xmax < x) xmax = x;
					if (ymax < y) ymax = y;
					if ((data[pos - 1] != blobs[i].label) || (data[pos + 1] != blobs[i].label) || (data[pos - bytesperline] != blobs[i].label) || (data[pos + bytesperline] != blobs[i].label))
					{
						blobs[i].perimeter++;
					}
				}
			}
		}


		blobs[i].x = xmin;
		blobs[i].y = ymin;
		blobs[i].width = (xmax - xmin) + 1;
		blobs[i].height = (ymax - ymin) + 1;
		blobs[i].xc = sumx / MAX(blobs[i].area, 1);
		blobs[i].yc = sumy / MAX(blobs[i].area, 1);
	}

	return 1;
}



int vc_binary_erode(IVC* src, IVC* dst, int kernel)
{
	unsigned char* datasrc = (unsigned char*)src->data;
	unsigned char* datadst = (unsigned char*)dst->data;
	int width = src->width;
	int height = src->height;
	int bytesperline = src->bytesperline;
	int channels = src->channels;
	int x, y, kx, ky;
	int offset = (kernel - 1) / 2;
	long int pos, posk;
	int blackFound;

	for (y = 0; y < height; y++)
	{
		for (x = 0; x < width; x++)
		{
			pos = y * bytesperline + x * channels;
			if ((int)datasrc[pos] != 0)
			{				for (ky = -offset, blackFound = 0; ky <= offset && !blackFound; ky++)
				{
					for (kx = -offset; kx <= offset && !blackFound; kx++)
					{
						if ((y + ky >= 0) && (y + ky < height) && (x + kx >= 0) && (x + kx < width))
						{
							posk = (y + ky) * bytesperline + (x + kx) * channels;
							if ((int)datasrc[posk] == 0) blackFound = 1;
						}
					}
				}
				if (blackFound) datadst[pos] = (unsigned char)0;
				else datadst[pos] = (unsigned char)255;
			}
			else datadst[pos] = (unsigned char)0;
		}
	}

	return 1;
}

int rgb_hsv(IVC* src, IVC* dst)
{
	unsigned char* datasrc = (unsigned char*)src->data;
	int bytesperline_src = src->width * src->channels;
	int channels_src = src->channels;
	unsigned char* datadst = (unsigned char*)dst->data;
	int bytesperline_dst = dst->width * dst->channels;
	int channels_dst = dst->channels;
	int width = src->width;
	int height = src->height;
	int x, y;
	long int pos_src, pos_dst;
	float r, g, b;
	float v, m, s, h;


	for (y = 0; y < height; y++)
	{
		for (x = 0; x < width; x++)
		{
			pos_src = y * bytesperline_src + x * channels_src;
			pos_dst = y * bytesperline_dst + x * channels_dst;

			r = (float)datasrc[pos_src];
			g = (float)datasrc[pos_src + 1];
			b = (float)datasrc[pos_src + 2];

			v = MAX(r, g, b);
			m = MIN(r, g, b);

			if (v == 0 || m == v)
			{
				s = 0;
				h = 0;
			}
			else
			{
				s = ((v - m) / v) * 255;
			}

			if (v == r)
			{
				if (g >= b)
				{
					h = (60 * (g - b) / (v - m)) / 360 * 255;
				}
				else
				{
					h = (360 + 60 * (g - b) / (v - m)) / 360 * 255;
				}
			}
			else if (v == g)
			{
				h = (120 + 60 * (b - r) / (v - m)) / 360 * 255;
			}
			else
			{
				h = (240 + 60 * (r - g) / (v - m)) / 360 * 255;
			}

			datadst[pos_dst] = h;
			datadst[pos_dst + 1] = s;
			datadst[pos_dst + 2] = v;
		}
	}

	return 1;
}
